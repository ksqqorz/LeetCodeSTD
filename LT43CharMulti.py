    def CharAdd(self,A,B):
        LengA,LengB=len(A),len(B)
        A=''.join(['0'*(LengB-LengA),A])
        Re=''
        AD = 0
        for i in range(LengB-1,-1,-1):
            if AD:
                P = self.AddMapJinWei[(A[i], B[i])]
                if len(P) > 1:
                    AD = 1
                else:
                    AD = 0
            else:
                P = self.AddMap[(A[i], B[i])]
                if len(P) > 1:
                    AD = 1
                else:
                    AD = 0
            Re=''.join([P[-1],Re])
        if AD:Re=''.join(['1',Re])
        return Re
